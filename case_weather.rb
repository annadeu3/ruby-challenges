weather = 'hot'
case weather    
when 'sunny'
puts "make sure to wear sunscreen and sunglasses!"
when 'cloudy'
puts "ditch the shades!"
when 'foggy'
puts "wear a sweater!"   
when 'rainy'
puts "time to rock those yellow rainboots!"
when 'snowy'
puts "make sure to wear those extra layers!"
when 'hot'
puts "daisy dukes, maybe?"
else
puts "dress accordingly to the forecast!"
end
